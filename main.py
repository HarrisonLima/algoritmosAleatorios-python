def SomaQuadrados():
    valor1 = int(input("Digite o primeiro valor: "))
    valor2 = int(input("Digite o segundo valor: "))
    valor3 = int(input("Digite o terceiro valor: "))

    resultado = valor1 + valor2 + valor3

    input(f'Resultado: { resultado:.2f }')

if __name__ == '__main__':
    SomaQuadrados()

